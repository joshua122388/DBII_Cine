    private void btnAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarActionPerformed
    int yearoSeleccionado = jYearChooserFiltro.getYear(); // Obtener el año elegido

    try (Connection conn = ConexionSQL.conectar()) {
        String query = "SELECT * FROM vw_reporte_venta_tiquetes WHERE YEAR(Fecha_Compra) = ? ORDER BY Cantidad DESC;";
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setInt(1, yearoSeleccionado); // Establecer parámetro del año
        ResultSet rs = stmt.executeQuery();

        // Modelo con columnas específicas
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID_Compra", "Cantidad", "Fecha_Compra", "ID_Funcion", "Pelicula", "Sala"}, 0
        );

        // Llenar la tabla con los resultados
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("ID_Compra"),
                rs.getInt("Cantidad"),
                rs.getDate("Fecha_Compra"),
                rs.getInt("ID_Funcion"),
                rs.getString("Pelicula"),
                rs.getInt("Sala")
            });
        }

        tblReporteVentaTiquetes.setModel(model);

        rs.close();
        stmt.close();
        JOptionPane.showMessageDialog(this, "Reporte generado para el año " + yearoSeleccionado);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al aplicar filtro por año: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnAplicarActionPerformed

    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
    try {
        // Crear un libro de Excel
        org.apache.poi.ss.usermodel.Workbook workbook = new org.apache.poi.xssf.usermodel.XSSFWorkbook();
        org.apache.poi.ss.usermodel.Sheet sheet = workbook.createSheet("Reporte de Ventas");

        // Encabezados
        org.apache.poi.ss.usermodel.Row headerRow = sheet.createRow(0);
        for (int i = 0; i < tblReporteVentaTiquetes.getColumnCount(); i++) {
            headerRow.createCell(i).setCellValue(tblReporteVentaTiquetes.getColumnName(i));
        }

        // Datos
        for (int i = 0; i < tblReporteVentaTiquetes.getRowCount(); i++) {
            org.apache.poi.ss.usermodel.Row row = sheet.createRow(i + 1);
            for (int j = 0; j < tblReporteVentaTiquetes.getColumnCount(); j++) {
                Object value = tblReporteVentaTiquetes.getValueAt(i, j);
                row.createCell(j).setCellValue(value != null ? value.toString() : "");
            }
        }

        // Diálogo para elegir ruta y nombre de archivo
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Guardar reporte como...");
        fileChooser.setSelectedFile(new java.io.File("ReporteVentasTiquetes.xlsx"));

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File fileToSave = fileChooser.getSelectedFile();
            try (java.io.FileOutputStream fileOut = new java.io.FileOutputStream(fileToSave)) {
                workbook.write(fileOut);
                workbook.close();
                JOptionPane.showMessageDialog(this, "Archivo guardado exitosamente en: \n" + fileToSave.getAbsolutePath());
            }
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al exportar Excel: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnExportarActionPerformed

