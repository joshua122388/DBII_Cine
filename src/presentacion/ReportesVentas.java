    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
  try {
        // Crear documento PDF
        com.lowagie.text.Document document = new com.lowagie.text.Document();

        // Abrir JFileChooser para elegir ubicación y nombre del archivo
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Guardar reporte como PDF");
        fileChooser.setSelectedFile(new java.io.File("ReporteVentasTiquetes.pdf"));

        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection != javax.swing.JFileChooser.APPROVE_OPTION) return;

        // Obtener el archivo seleccionado por el usuario
        java.io.File fileToSave = fileChooser.getSelectedFile();

        // Crear el writer para generar el archivo PDF
        com.lowagie.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(fileToSave));
        document.open();

        // Agregar título
        com.lowagie.text.Paragraph title = new com.lowagie.text.Paragraph("Reporte de Venta de Tiquetes\n\n");
        title.setAlignment(com.lowagie.text.Element.ALIGN_CENTER);
        document.add(title);

        // Crear tabla PDF con el mismo número de columnas que la JTable
        com.lowagie.text.pdf.PdfPTable pdfTable = new com.lowagie.text.pdf.PdfPTable(tblReporteVentaTiquetes.getColumnCount());
        pdfTable.setWidthPercentage(100);

        // Agregar encabezados de columna
        for (int col = 0; col < tblReporteVentaTiquetes.getColumnCount(); col++) {
            pdfTable.addCell(tblReporteVentaTiquetes.getColumnName(col));
        }

        // Agregar datos de las filas
        for (int row = 0; row < tblReporteVentaTiquetes.getRowCount(); row++) {
            for (int col = 0; col < tblReporteVentaTiquetes.getColumnCount(); col++) {
                Object value = tblReporteVentaTiquetes.getValueAt(row, col);
                pdfTable.addCell(value != null ? value.toString() : "");
            }
        }

        // Agregar la tabla al documento y cerrar
        document.add(pdfTable);
        document.close();

        JOptionPane.showMessageDialog(this, "Reporte exportado exitosamente:\n" + fileToSave.getAbsolutePath());

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al exportar PDF: " + e.getMessage());
    }
    }//GEN-LAST:event_btnPDFActionPerformed

