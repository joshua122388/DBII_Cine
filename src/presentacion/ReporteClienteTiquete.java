    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
    Document document = new Document();
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Guardar reporte como...");
    fileChooser.setSelectedFile(new File("ClientesEstrella.pdf"));
    
    int userSelection = fileChooser.showSaveDialog(this);
    
    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToSave = fileChooser.getSelectedFile();
        try {
            PdfWriter.getInstance(document, new FileOutputStream(fileToSave));
            document.open();
            document.add(new Paragraph("Reporte de Clientes Estrella"));
            document.add(new Paragraph(" ")); // espacio

            PdfPTable table = new PdfPTable(tblReporteClientes.getColumnCount());
            
            // Agregar encabezados
            for (int i = 0; i < tblReporteClientes.getColumnCount(); i++) {
                table.addCell(tblReporteClientes.getColumnName(i));
            }

            // Agregar filas
            for (int row = 0; row < tblReporteClientes.getRowCount(); row++) {
                for (int col = 0; col < tblReporteClientes.getColumnCount(); col++) {
                    Object value = tblReporteClientes.getValueAt(row, col);
                    table.addCell(value != null ? value.toString() : "");
                }
            }

            document.add(table);
            document.close();

            JOptionPane.showMessageDialog(this, "Archivo PDF guardado exitosamente en:\n" + fileToSave.getAbsolutePath());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al exportar a PDF: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnPDFActionPerformed

