    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
    try {
        // Crear el libro de Excel
        Workbook workbook = new XSSFWorkbook();
        Sheet sheet = workbook.createSheet("Clientes Estrella");

        // Crear fila de encabezado
        Row headerRow = sheet.createRow(0);
        for (int i = 0; i < tblReporteClientes.getColumnCount(); i++) {
            Cell cell = headerRow.createCell(i);
            cell.setCellValue(tblReporteClientes.getColumnName(i));
        }

        // Agregar los datos
        for (int i = 0; i < tblReporteClientes.getRowCount(); i++) {
            Row row = sheet.createRow(i + 1);
            for (int j = 0; j < tblReporteClientes.getColumnCount(); j++) {
                Object value = tblReporteClientes.getValueAt(i, j);
                row.createCell(j).setCellValue(value != null ? value.toString() : "");
            }
        }

        // Diálogo para guardar archivo
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Guardar reporte como...");
        fileChooser.setSelectedFile(new File("ClientesEstrella.xlsx"));
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            try (FileOutputStream fileOut = new FileOutputStream(fileToSave)) {
                workbook.write(fileOut);
                workbook.close();
                JOptionPane.showMessageDialog(this, "Archivo guardado exitosamente en:\n" + fileToSave.getAbsolutePath());
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al exportar: " + e.getMessage());
    }

    // Ir al menú principal
    MenuAdmin menu = new MenuAdmin();
    menu.setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnExportarActionPerformed

